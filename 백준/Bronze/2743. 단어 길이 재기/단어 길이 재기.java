//2743. 단어 길이 재기
//문자열 생성 및 입력->문자열 길이 출력
import java.util.Scanner;

public class Main {
    public static void main(String[] args){

        //입력 및 문자열 word 생성
        Scanner sc = new Scanner(System.in);
        String word = sc.next();

        //문자열 길이 출력(.length())
        System.out.println(word.length());
    }
}
